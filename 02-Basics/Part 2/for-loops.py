# For loops
for item in 'Zero to Mastery':
    print(item)

for item in [1, 2, 3, 4, 5]:
    print("list", item)

for item in {1, 2, 3, 4, 5}:
    print("set", item)

for item in (1, 2, 3, 4, 5):
    print("tuple", item)

print(item)

for item in (1, 2, 3, 4, 5):
    for x in ['a', 'b', 'c']:
        print(item, x)
